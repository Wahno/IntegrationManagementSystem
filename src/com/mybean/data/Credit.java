package com.mybean.data;

public class Credit {
	
	private String customer;
	private String sponsor;
	private String sponsorAndCostomer;
	public String getCustomer() {
		return customer;
	}
	public void setCustomer(String customer) {
		this.customer = customer;
	}
	public String getSponsor() {
		return sponsor;
	}
	public void setSponsor(String sponsor) {
		this.sponsor = sponsor;
	}
	public String getSponsorAndCostomer() {
		return sponsorAndCostomer;
	}
	public void setSponsorAndCostomer(String sponsorAndCostomer) {
		this.sponsorAndCostomer = sponsorAndCostomer;
	}

}
